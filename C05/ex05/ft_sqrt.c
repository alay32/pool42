int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 2)
		return (0);
	else
	{
		while (i < nb / 2 && i <= 46340)
		{
			if (nb % i == 0 && i * i == nb)
				return (i);
		i++;
		}
	}
	return (0);
}
